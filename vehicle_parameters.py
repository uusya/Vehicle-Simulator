import numpy as np

class VehicleParameters:
    """
    Класс для хранения параметров автомобиля линейной модели 'велосипед'
    Типичные значения для легкового автомобиля
    """
    
    def __init__(self):
        # Основные геометрические параметры
        self.m = 1200.0           # масса, кг
        self.J_z = 1500.0         # момент инерции, кг·м²
        self.a = 1.4              # расстояние до передней оси, м
        self.b = 1.6              # расстояние до задней оси, м
        self.L = self.a + self.b  # колесная база, м
        
        # Жесткости шин
        self.C_f = 80000.0        # угловая жесткость передних шин, Н/рад
        self.C_r = 100000.0       # угловая жесткость задних шин, Н/рад
        
        # Скоростные параметры
        self.V = 20.0             # продольная скорость, м/с
        
    def validate_parameters(self):
        """Проверка корректности параметров"""
        assert self.m > 0, "Масса должна быть положительной"
        assert self.J_z > 0, "Момент инерции должен быть положительным"
        assert self.a > 0 and self.b > 0, "Расстояния до осей должны быть положительными"
        assert self.C_f > 0 and self.C_r > 0, "Жесткости шин должны быть положительными"
        assert self.V > 0, "Скорость должна быть положительной"
        
        print("✓ Все параметры корректны")
        return True
        
    def get_summary(self):
        """Возвращает текстовое описание параметров"""
        return f"""
Параметры автомобиля:
- Масса: {self.m} кг
- Момент инерции: {self.J_z} кг·м²  
- Колесная база: {self.L:.1f} м (a={self.a:.1f} м, b={self.b:.1f} м)
- Жесткость передних шин: {self.C_f} Н/рад
- Жесткость задних шин: {self.C_r} Н/рад
- Скорость: {self.V} м/с ({self.V * 3.6:.1f} км/ч)
        """

# Для быстрого создания стандартных параметров
def create_default_parameters():
    """Создает объект параметров с настройками по умолчанию"""
    return VehicleParameters()